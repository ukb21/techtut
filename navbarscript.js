// jquery for hovering images
$( document ).ready( function() {
	$( ".upsm" ).hover( function() {
		$( this ).attr( "src", "rmwhite.jpg" );
	}, function() {
		$( this ).attr( "src", "rmgray.jpg" );
	} );

} );

